
package rft;


public class Pre {
    
    private String nome;
    private int dia;
    private String mes;
    private String presenca;

    public Pre() {
    }

    public Pre(String nome, int dia, String mes, String presenca) {
        this.nome = nome;
        this.dia = dia;
        this.mes = mes;
        this.presenca = presenca;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getPresenca() {
        return presenca;
    }

    public void setPresenca(String presenca) {
        this.presenca = presenca;
    }
    
    
   
    
    
    
    }
    

