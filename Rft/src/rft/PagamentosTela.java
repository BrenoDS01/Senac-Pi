    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        
     int linhaSelecionada = tabelaP.getSelectedRow();

    if (linhaSelecionada >= 0) {
        String nome = tabelaP.getValueAt(linhaSelecionada, 0).toString(); 

        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir esse registro?");
        if (resposta == JOptionPane.YES_OPTION) {
            if (LoginFuncao.excluirp(nome)) {
                JOptionPane.showMessageDialog(null, "Registro exclu√≠do com sucesso.");
                tabelaP.setModel(montarTabelaP()); 
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao excluir registro.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma linha para excluir.");
    }   
        
    }//GEN-LAST:event_btnDeletarActionPerformed

