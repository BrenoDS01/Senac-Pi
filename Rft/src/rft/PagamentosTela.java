    private DefaultTableModel montarTabelaP() {
        LoginFuncao dao = new LoginFuncao();
        String[] colunas = {"Nome", "Tipo Plano", "Valor", "Pago"};

       
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);
        
        

        List<Pagamentos> lista = LoginFuncao.listarPagamentos();
        

        for (int i = 0; i < lista.size(); i++) {
            Pagamentos pa = lista.get(i);

            String[] linha = {
                
                pa.getNome(),
                pa.getTipoplano(),
                Integer.toString(pa.getValor()),
                pa.getPago()
                
                
            };
            tabela.addRow(linha);
        }
        
        return tabela;
    }
    
     private DefaultTableModel montarTabelaBuscaP(String busca) {
        String[] colunas = {"Nome", "Tipo Plano", "Valor", "Pago"};

        //Criamos a tabela
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        List<Pagamentos> lista = LoginFuncao.buscarPorNomeP(busca);

        for (int i = 0; i < lista.size(); i++) {
             Pagamentos pa = lista.get(i);

            String[] linha = {
                
               pa.getNome(),
                pa.getTipoplano(),
                Integer.toString(pa.getValor()),
                pa.getPago()
                
            };
            tabela.addRow(linha);
        }

        if (tabela.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum aluno encontrado com esse nome!", "Filtro", JOptionPane.INFORMATION_MESSAGE);
        }

        return tabela;
    }
    
    private int getPosicao() {
        int posicao = tabelaP.getSelectedRow();
        if (posicao <= -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item para excluir.");
        }
        return Integer.parseInt((String) tabelaP.getValueAt(posicao, 0));
    }

